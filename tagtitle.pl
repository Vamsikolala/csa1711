show_html :-
    write('<!DOCTYPE html>'), nl,
    write('<html>'), nl,
    write('<head>'), nl,
    write('<title>My Blog Page</title>'), nl,
    write('</head>'), nl,
    write('<body>'), nl,
    write('<h1>Welcome to My Prolog Blog</h1>'), nl,
    write('<p>This is a simple example of HTML tags using Prolog.</p>'), nl,
    write('<a href="https://example.com" target="_blank">Visit Example</a>'), nl,
    write('</body>'), nl,
    write('</html>'), nl.
